public class HW07_3__1 {
    public static int addition(int a, int b) {
        return a + b;
    }
    public static int substruction(int a, int b) {
        return a - b;
    }
    public static int multiplication(int a, int b) {
        return a * b;
    }
    public static int division(int a, int b){
        return a / b;
    }

    public static void main(String[] args) {
        System.out.println(addition(15,20));
        System.out.println(substruction(40,23));
        System.out.println(multiplication(27,8));
        System.out.println(division(140,7));
    }
}
